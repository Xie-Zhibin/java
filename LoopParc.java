public class LoopParc {
	public static void main (String[] args){
		int i;
		// for loop
		for (i=0;i<100;i++){
			System.out.println("test" + i);
		}
		i = 0;

		// while loop
		while (i<100){
			System.out.println("Test" + i);
		    i++;
		}
		i = 0;

		//do while loop
		do{
			System.out.println("hahahh" + i);
			i++;
		}
		while(i<100);
	}
}
